defmodule ArmoricoreRealtime.Content.Tag do
  @moduledoc """
  Tag schema for labeling videos.
  """

  use Ecto.Schema
  import Ecto.Changeset

  @primary_key {:id, :binary_id, autogenerate: true}
  @foreign_key_type :binary_id

  schema "tags" do
    field :name, :string
    many_to_many :videos, ArmoricoreRealtime.Content.Video, join_through: "video_tags"

    timestamps(type: :utc_datetime)
  end

  @doc false
  def changeset(tag, attrs) do
    tag
    |> cast(attrs, [:name])
    |> validate_required([:name])
    |> validate_length(:name, max: 50)
    |> unique_constraint(:name)
  end
end

