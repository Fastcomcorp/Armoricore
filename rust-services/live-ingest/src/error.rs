use thiserror::Error;

#[derive(Error, Debug)]
pub enum LiveIngestError {
    #[error("Configuration error: {0}")]
    Config(String),

    #[error("RTMP server error: {0}")]
    RtmpServer(String),

    #[error("Stream key validation error: {0}")]
    StreamKeyValidation(String),

    #[error("Stream manager error: {0}")]
    StreamManager(String),

    #[error("HTTP error: {0}")]
    Http(String),

    #[error("IO error: {0}")]
    Io(#[from] std::io::Error),

    #[error("Serialization error: {0}")]
    Serialization(#[from] serde_json::Error),
}

pub type Result<T> = std::result::Result<T, LiveIngestError>;

